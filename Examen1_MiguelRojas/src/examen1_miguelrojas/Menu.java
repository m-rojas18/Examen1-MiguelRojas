            String nombreNew = "";
            int edadNew;
            String carreraNew = "";
            String lugarNew = "";
            int num_CuentaNew;
            String usuario = "";
            String password = "";

            //Cambio
            nombreNew = tf_nombreMOD.getText();
            edadNew = Integer.parseInt(tf_edadMOD.getText());
            carreraNew = tf_carreraMOD.getText();
            lugarNew = tf_lugarMOD.getText();
            num_CuentaNew = Integer.parseInt(tf_num_cuentaMOD.getText());
